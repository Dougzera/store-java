  public ResultSet search(String data) {
    ResultSet rs = null;
    String SQL = "SELECT p.data_atlz DATA, sum(p.preco_und) SOMA\n" +
                 "FROM produto p\n" +
                 "WHERE date_format(p.data_atlz,'%m-%Y') = '" +data+ "'\n" +
                 "GROUP BY p.data_atlz";
    try {
      rs = stmt.executeQuery(SQL);
    } catch (SQLException e) {
      e.printStackTrace();
    }
    return rs;
   
    }
  
  
